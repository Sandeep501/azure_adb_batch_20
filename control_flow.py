# conditional statements

# if condition is true:
#     execute the code inside this block
# else:
#     execute this block of code

x = 20

if x > 11:
    print(x, "is greater then 11")  
elif x < 100:
    print(x, "is less then 11")
elif x == 11:
    print(x, "is equal to 11")
else:
    print("Something went wrong")
    

